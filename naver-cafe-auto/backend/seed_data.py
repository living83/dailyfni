def seed(conn: sqlite3.Connection):
    """키워드와 댓글 템플릿이 비어 있을 때만 시드 데이터 삽입"""
    cursor = conn.cursor()

    kw_count = cursor.execute("SELECT COUNT(*) FROM keywords").fetchone()[0]
    ct_count = cursor.execute("SELECT COUNT(*) FROM comment_templates").fetchone()[0]

    if kw_count > 0 or ct_count > 0:
        return  # 이미 데이터 있으면 스킵

    # ── 1) 댓글 템플릿 삽입 (중복 제거) ──
    all_texts = set()
    for groups in CATEGORY_COMMENT_GROUPS.values():
        for group in groups:
            all_texts.update(group)

    tpl_ids = {}
    for text in sorted(all_texts):
        cursor.execute("INSERT INTO comment_templates (text) VALUES (?)", (text,))
        tpl_ids[text] = cursor.lastrowid

    # ── 2) 키워드 삽입 + 매핑 ──
    kw_total = 0
    mapping_total = 0

    for category, keywords in KEYWORD_CATEGORIES.items():
        comment_groups = CATEGORY_COMMENT_GROUPS[category]

        for kw in keywords:
            cursor.execute("INSERT INTO keywords (text) VALUES (?)", (kw,))
            kw_id = cursor.lastrowid
            kw_total += 1

            # 해당 카테고리의 댓글 그룹 전체 매핑
            seen = set()
            for group in comment_groups:
                for text in group:
                    tid = tpl_ids[text]
                    if tid not in seen:
                        cursor.execute(
                            "INSERT INTO keyword_comment_mapping (keyword_id, comment_template_id) VALUES (?, ?)",
                            (kw_id, tid),
                        )
                        seen.add(tid)
                        mapping_total += 1

    conn.commit()
    print(f"[seed] 키워드 {kw_total}개, 댓글 {len(tpl_ids)}개, 매핑 {mapping_total}개 삽입 완료")


def reseed(conn: sqlite3.Connection):
    """기존 키워드·댓글·매핑을 모두 삭제하고 초기화"""
    cursor = conn.cursor()
    cursor.execute("UPDATE publish_history SET keyword_id = NULL")
    cursor.execute("DELETE FROM keyword_comment_mapping")
    cursor.execute("DELETE FROM keyword_board_mapping")
    cursor.execute("DELETE FROM comment_templates")
    cursor.execute("DELETE FROM keywords")
    conn.commit()
